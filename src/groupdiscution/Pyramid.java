package groupdiscution;

public class Pyramid {
    public static void main(String[] args) {



        for (int i=0; i<10;i++){
            for (int z=9;z>=i;z--){
                System.out.print(" ");
            }for (int x=0;x<=i;x++){
                System.out.print(" *");
            }
            System.out.println("");
        }


        }

}


